package OneNeuronAssigment1;

public class SumOfNumber {

	public static void main(String[] args) {
		
		  double sum[]= {10,90.78,111,8989,7876};
		  
		  double add=0;
		  
		  for(int i=0;i<sum.length;i++)
		  
		  {
		  
		  add=add+sum[i]; 
		  
		  }
		 
		  System.out.println("Sum of adding values:" +add);

	}

}
